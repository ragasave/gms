    <script>
        document.querySelector('body').addEventListener('click', function(e){
            if(e.target.classList.contains('xx-read-more')){
                e.preventDefault()
                let elem = e.target;
                let p = elem.parentElement.parentElement.parentElement.lastElementChild;
                let wpr = p.querySelector('.xx-more-data-wpr');
                if(p.classList.contains('show')){
                    p.classList.remove('show');
                    elem.textContent = 'Read More';
                    p.style.height = 0 + "px";
                }else{
                    p.classList.add('show');
                    p.style.height = wpr.offsetHeight + "px";
                    elem.textContent = 'Read Less';
                }
            }
        });
    </script>

